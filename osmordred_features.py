def _safe_calculate(func, molecule: Mol, *args):
    try:
        return np.array(func(molecule, *args), dtype=DTYPE)
    except Exception as e:
        key = func.__name__[4:]
        smiles = "<invalid>"
        try:
            smiles = Chem.MolToSmiles(molecule)
        except:  # i can't believe this, but the reversion to SMILES can actually fail
            pass
        logger.warning(f"Descriptor {key} failed for SMILES {smiles}: {repr(e)}")
        return np.full(_DESCRIPTOR_SIZES[key], np.nan, dtype=DTYPE)


