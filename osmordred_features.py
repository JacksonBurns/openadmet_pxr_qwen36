def _validate_smiles(smi: str) -> str:
    """Return SMILES if valid, else empty string."""
    if smi is None:
        return ""
    try:
        return smi if Chem.MolFromSmiles(smi) is not None else ""
    except Exception:
        return ""


if __name__ == "__main__":
    # this uses osmordred, except modified to that anything that would be printed to std::cout instead goes to std:cerr
    # (done with vscode using (?<!//)std::cout ->> std::cerr regex-based search and replace)
    #
    # this is done in my fork here: https://github.com/JacksonBurns/osmordred/tree/65e7dd40cc8209d695d98838dff2f346
    #
    # python OSMOrdredCalculator.py molpile_smiles.parquet /datai/osmordred_molpile_output.parquet 2> /datai/molpile_osmordred_errors.log
    #
    # also, for functions using solveLinearSystem I have them throw an error instead of returning 5 zeros
    import os
    import sys
    from concurrent.futures import ProcessPoolExecutor
    from multiprocessing import Pool
    from pathlib import Path

    import numpy as np
    import polars as pl
    from rdkit import Chem
    from rdkit.rdBase import BlockLogs
    from tqdm import tqdm

    with BlockLogs():
        try:
            in_file = Path(sys.argv[1])
            out_file = Path(sys.argv[2]) # Expecting a single file path again
            i = int(sys.argv[3])
        except Exception:
            print("usage: python _osmordred.py </path/to/input_file.csv> </path/to/output_file.parquet> <start_index>")
            sys.exit(1)

        df = pl.read_csv(in_file, columns=["SMILES"])

        smiles = df["SMILES"].to_list()
        # Run RDKit validation in parallel across all cores
        with ProcessPoolExecutor(max_workers=os.cpu_count()) as ex:
            cleaned_smiles = list(
                tqdm(
                    ex.map(_validate_smiles, smiles, chunksize=1000),
                    total=len(smiles),
                    desc="Validating SMILES",
                )
            )

        # Write validated SMILES alongside the final output directory
        out_file.parent.mkdir(parents=True, exist_ok=True)
        pl.DataFrame({"SMILES": cleaned_smiles}).write_parquet(out_file.parent / f"{in_file.stem}_validated_smiles.parquet")
        
        del smiles, cleaned_smiles

        n_mols = df.shape[0]
        chunk_rows = 512

        # List to store our computed chunks in memory
        accumulated_chunks = []

        p = Pool(64)
        with tqdm(total=n_mols, initial=i, desc=f"Calculating Features (batch size {chunk_rows})", file=sys.stdout) as pbar:
            while i < n_mols:
                end_idx = min(i + chunk_rows, n_mols)
                batch_smiles = df["SMILES"][i:end_idx].to_list()
                
                # Calculate batch features
                batch_data = np.stack(p.map(calculate, batch_smiles))
                
                # Convert the NumPy matrix to a Polars DataFrame
                chunk_df = pl.DataFrame(batch_data, schema=DESCRIPTOR_NAMES, orient="row")
                chunk_df = chunk_df.with_columns(pl.Series("SMILES", batch_smiles))
                
                # Store the DataFrame in memory instead of writing to disk
                accumulated_chunks.append(chunk_df)
                
                pbar.update(end_idx - i)
                i = end_idx

        # Concatenate everything and write to a single file at the end
        print(f"\nConcatenating {len(accumulated_chunks)} chunks and saving to {out_file}...")
        final_df = pl.concat(accumulated_chunks)
        final_df.write_parquet(out_file)
        print("Done!")
